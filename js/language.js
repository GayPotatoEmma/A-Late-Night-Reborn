  // Load saved language or default to 'en'
  function loadLanguage() {
    try {
      var saved = localStorage.getItem(STORAGE_KEY);
      if (saved === 'de' || saved === 'en') {
        currentLang = saved;
      }
    } catch (e) {}
    return currentLang;
  }

  // Save language to localStorage
  function saveLanguage(lang) {
    currentLang = lang;
    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch (e) {}
  }

  // Apply translations to the page
  function translatePage(lang) {
    var trans = translations[lang] || translations['en'];

    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      var key = el.getAttribute('data-i18n');
      if (trans[key]) {
        el.textContent = trans[key];
      }
    });

    // Translate all elements with data-i18n-html attribute (for innerHTML)
    document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
      var key = el.getAttribute('data-i18n-html');
      if (trans[key]) {
        el.innerHTML = trans[key];
      }
    });

    // Translate aria-label attributes
    document.querySelectorAll('[data-i18n-aria]').forEach(function(el) {
      var key = el.getAttribute('data-i18n-aria');
      if (trans[key]) {
        el.setAttribute('aria-label', trans[key]);
      }
    });

    // Translate title attributes
    document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
      var key = el.getAttribute('data-i18n-title');
      if (trans[key]) {
        el.setAttribute('title', trans[key]);
      }
    });
  }

  // Apply language (updates UI and translations)
  function applyLanguage(lang) {
    currentLang = lang;
    document.documentElement.setAttribute('lang', lang);

    // Update dropdown display
    var toggle = document.getElementById('language-toggle');
    if (toggle) {
      toggle.textContent = lang === 'en' ? 'EN' : 'DE';
      toggle.setAttribute('aria-label', translations[lang]['select-language']);
    }

    // Update dropdown options
    var enOption = document.getElementById('lang-en');
    var deOption = document.getElementById('lang-de');
    if (enOption && deOption) {
      if (lang === 'en') {
        enOption.classList.add('selected');
        deOption.classList.remove('selected');
      } else {
        enOption.classList.remove('selected');
        deOption.classList.add('selected');
      }
    }

    // Apply translations to page
    translatePage(lang);
  }

  // Initialize on page load
  document.addEventListener('DOMContentLoaded', function () {
    var lang = loadLanguage();
    applyLanguage(lang);

    // Toggle dropdown visibility
    var toggle = document.getElementById('language-toggle');
    var dropdown = document.getElementById('language-dropdown');

    if (toggle && dropdown) {
      toggle.addEventListener('click', function (e) {
        e.stopPropagation();
        dropdown.classList.toggle('open');
        toggle.setAttribute('aria-expanded', dropdown.classList.contains('open'));
      });

      // Close dropdown when clicking outside
      document.addEventListener('click', function (e) {
        if (!toggle.contains(e.target) && !dropdown.contains(e.target)) {
          dropdown.classList.remove('open');
          toggle.setAttribute('aria-expanded', 'false');
        }
      });

      // Language selection
      var enOption = document.getElementById('lang-en');
      var deOption = document.getElementById('lang-de');

      if (enOption) {
        enOption.addEventListener('click', function () {
          applyLanguage('en');
          saveLanguage('en');
          dropdown.classList.remove('open');
          toggle.setAttribute('aria-expanded', 'false');
        });
      }

      if (deOption) {
        deOption.addEventListener('click', function () {
          applyLanguage('de');
          saveLanguage('de');
          dropdown.classList.remove('open');
          toggle.setAttribute('aria-expanded', 'false');
        });
      }
    }
  });
